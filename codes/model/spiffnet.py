class eca_layer_1d(nn.Module):
    """Constructs a ECA module.
    Args:
        channel: Number of channels of the input feature map
        k_size: Adaptive selection of kernel size
    """
    def __init__(self, channel, k_size=3):
        super(eca_layer_1d, self).__init__()
        self.avg_pool = nn.AdaptiveAvgPool1d(1)
        self.conv = nn.Conv1d(1, 1, kernel_size=k_size, padding=(k_size - 1) // 2, bias=False)
        self.sigmoid = nn.Sigmoid()
        self.channel = channel
        self.k_size =k_size

    def forward(self, x):
        # b hw c
        # feature descriptor on the global spatial information
        y = self.avg_pool(x.transpose(-1, -2))

        # Two different branches of ECA module
        y = self.conv(y.transpose(-1, -2))

        # Multi-scale information fusion
        y = self.sigmoid(y)

        return x * y.expand_as(x)

    def flops(self):
        flops = 0
        flops += self.channel*self.channel*self.k_size

        return flops


class LeFF(nn.Module):
    def __init__(self, dim=64, hidden_dim=128, act_layer=nn.GELU,drop = 0., use_eca=False):
        super().__init__()
        self.linear1 = nn.Sequential(nn.Linear(dim, hidden_dim),
                                     act_layer())
        self.dwconv = nn.Sequential(nn.Conv2d(hidden_dim,hidden_dim,groups=hidden_dim,kernel_size=3,stride=1,padding=1),
                                    act_layer())
        self.linear2 = nn.Sequential(nn.Linear(hidden_dim, dim))
        self.dim = dim
        self.hidden_dim = hidden_dim
        self.eca = eca_layer_1d(dim) if use_eca else nn.Identity()

    def forward(self, x):
        # bs x hw x c
        bs, hw, c = x.size()
        hh = int(math.sqrt(hw))

        x = self.linear1(x)

        # spatial restore
        x = rearrange(x, ' b (h w) (c) -> b c h w ', h = hh, w = hh)
        # bs,hidden_dim,32x32

        x = self.dwconv(x)

        # flaten
        x = rearrange(x, ' b c h w -> b (h w) c', h = hh, w = hh)

        x = self.linear2(x)
        x = self.eca(x)

        return x

    def flops(self, H, W):
        flops = 0
        # fc1
        flops += H*W*self.dim*self.hidden_dim
        # dwconv
        flops += H*W*self.hidden_dim*3*3
        # fc2
        flops += H*W*self.hidden_dim*self.dim
        print("LeFF:{%.2f}"%(flops/1e9))
        # eca
        if hasattr(self.eca, 'flops'):
            flops += self.eca.flops()
        return flops


def global_patch_divide(x, ps, x_size=(64,64)):
    B, L, C = x.shape
    H, W = x_size
    x_all = x.view(B, H, W, C)
    G = ps
    ip = (H // G) * G
    num = 0
    if H % G == 0:
        x_LDA = x.view(B, G, H // G, G, W // G, C).permute(0, 2, 4, 1, 3, 5).contiguous() #b nh nw ps ps c
        x_LDA = x_LDA.view(B, H * W // G ** 2, G ,G , C).contiguous() # b n ps ps C
        x_LDA = x_LDA.permute(0, 1, 4, 2, 3).contiguous()# b n C ps ps
    else:
        for iy in range(0, H, ip):
            if iy + ip > H:
                iy = H - ip
            for ix in range(0, W, ip):
                if ix + ip > W:
                    ix = W - ip
                num += 1
                x_part = x_all[:,iy:iy+ip, ix:ix+ip,:]
                x_LDA_part = x_part.view(B, G, ip // G, G, ip // G, C).permute(0, 2, 4, 1, 3, 5).contiguous()
                x_LDA_part = x_LDA_part.view(B, ip * ip // G ** 2, G ,G , C).contiguous() # b n ps ps C
                x_LDA_part = x_LDA_part.permute(0, 1, 4, 2, 3).contiguous()# b n C ps ps
                if num == 1:
                    x_LDA = x_LDA_part
                else:
                    x_LDA = torch.cat((x_LDA, x_LDA_part),1)

    return x_LDA

class channel_Attention(nn.Module):
    def __init__(self, dim, num_heads, bias):
        super(channel_Attention, self).__init__()
        self.num_heads = num_heads
        self.temperature = nn.Parameter(torch.ones(num_heads, 1, 1))

        self.q = nn.Conv2d(dim, dim, kernel_size=1, bias=bias)
        self.q_dwconv = nn.Conv2d(dim, dim, kernel_size=3, stride=1, padding=1, groups=dim, bias=bias)

        self.kv = nn.Conv2d(dim, dim * 2, kernel_size=1, bias=bias)
        self.kv_dwconv = nn.Conv2d(dim * 2, dim * 2, kernel_size=3, stride=1, padding=1, groups=dim * 2, bias=bias)
        self.project_out = nn.Conv2d(dim, dim, kernel_size=1, bias=bias)

    def forward(self, x, y, x_size=(64,64)):

        B, L, C = x.shape
        H, W = x_size
        x = x.view(B, H, W, C)
        x = x.permute(0, 3, 1, 2)
        y = y.view(B, H, W, C)
        y = y.permute(0, 3, 1, 2)

        b, c, h, w = x.shape
        q = self.q_dwconv(self.q(x))
        kv = self.kv_dwconv(self.kv(y))
        k, v = kv.chunk(2, dim=1)

        q = rearrange(q, 'b (head c) h w -> b head c (h w)', head=self.num_heads)
        k = rearrange(k, 'b (head c) h w -> b head c (h w)', head=self.num_heads)
        v = rearrange(v, 'b (head c) h w -> b head c (h w)', head=self.num_heads)

        q = torch.nn.functional.normalize(q, dim=-1)
        k = torch.nn.functional.normalize(k, dim=-1)

        attn = (q @ k.transpose(-2, -1)) * self.temperature
        attn = attn.softmax(dim=-1)

        out = (attn @ v)

        out = rearrange(out, 'b head c (h w) -> b (head c) h w', head=self.num_heads, h=h, w=w)

        out = self.project_out(out)

        out = rearrange(out, 'b (head c) h w  -> b (h w) (head c)', head=self.num_heads, h=h, w=w)

        return out

