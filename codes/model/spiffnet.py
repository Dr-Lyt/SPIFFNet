        # a_norm = np.linalg.norm(a, axis=1)

        attn = torch.matmul(q, q_y.transpose(-1, -2))  # (b, n, n)

        attn = attn * self.scale
        # self exclusion
        # attn = attn - 100 * torch.eye(n).unsqueeze(0).to(attn.device)

        if self.training:
            hard_attn = F.gumbel_softmax(attn, tau=1., hard=True, dim=-1)
